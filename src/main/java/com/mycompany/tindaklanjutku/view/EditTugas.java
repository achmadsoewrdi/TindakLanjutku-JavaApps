    private void roundedButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundedButton3ActionPerformed
String judul = judulField.getText();
    String deskripsi = deskripsiField.getText();
    Date deadline = dateDeadline.getDate();
    String kategori = (String) comboKategori.getSelectedItem();

    // Validasi input
    if (judul.isEmpty() || deskripsi.isEmpty() || deadline == null || kategori == null) {
        JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
        return;
    }

    try (Connection conn = Koneksi.configDB();
         PreparedStatement pstmt = conn.prepareStatement(
                 "UPDATE tugas SET judul = ?, deskripsi = ?, deadline = ?, id_kategori = "
                 + "(SELECT id_kategori FROM kategori WHERE nama_kategori = ?) "
                 + "WHERE id_tugas = ?")) {
        
        pstmt.setString(1, judul);
        pstmt.setString(2, deskripsi);
        pstmt.setDate(3, new java.sql.Date(deadline.getTime()));
        pstmt.setString(4, kategori);
        pstmt.setInt(5, idTugas);
        
        int rowsAffected = pstmt.executeUpdate();
        
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(this, "Tugas berhasil diperbarui!");
            new tugas().setVisible(true); // Kembali ke halaman tugas
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui tugas!");
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }        
    }//GEN-LAST:event_roundedButton3ActionPerformed

