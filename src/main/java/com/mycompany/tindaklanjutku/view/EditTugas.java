private void loadKategori() {
    String sql = "SELECT id_kategori, nama_kategori FROM kategori";
    
    try (Connection conn = Koneksi.configDB();
         PreparedStatement pstmt = conn.prepareStatement(sql);
         ResultSet rs = pstmt.executeQuery()) {
        
        comboKategori.removeAllItems();
        
        while (rs.next()) {
            comboKategori.addItem(rs.getString("nama_kategori"));
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal memuat kategori: " + e.getMessage());
    }
}

class KategoriItem {
    private int id;
    private String nama;

    public KategoriItem(int id, String nama) {
        this.id = id;
        this.nama = nama;
    }

    public int getId() {
        return id;
    }

    @Override
    public String toString() {
        return nama;
    }
}

private void loadTugasData() {
    try (Connection conn = Koneksi.configDB();
         PreparedStatement pstmt = conn.prepareStatement(
                 "SELECT judul, deskripsi, deadline, id_kategori FROM tugas WHERE id_tugas = ?")) {
        
        pstmt.setInt(1, idTugas);
        ResultSet rs = pstmt.executeQuery();
        
        if (rs.next()) {
            // Set nilai ke field
            judulField.setText(rs.getString("judul"));
            deskripsiField.setText(rs.getString("deskripsi"));
            dateDeadline.setDate(rs.getDate("deadline"));
            
            // Set combo box kategori
            int idKategori = rs.getInt("id_kategori");
            setSelectedKategori(idKategori);
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal memuat data tugas: " + e.getMessage());
    }
}

private void setSelectedKategori(int idKategori) {
    try (Connection conn = Koneksi.configDB();
         PreparedStatement pstmt = conn.prepareStatement(
                 "SELECT nama_kategori FROM kategori WHERE id_kategori = ?")) {
        
        pstmt.setInt(1, idKategori);
        ResultSet rs = pstmt.executeQuery();
        
        if (rs.next()) {
            String namaKategori = rs.getString("nama_kategori");
            comboKategori.setSelectedItem(namaKategori);
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
}


